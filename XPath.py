# Рекомендации по выбору XPath-локаторов
# ✅ Надёжные атрибуты (лучшие для использования)
# @id – идеальный вариант, если ID статичен и уникален
# @data-qa, @data-testid – специальные атрибуты для тестов, самые стабильные
# @aria-label – атрибут доступности, редко изменяется
# @name – хорошо подходит для полей форм
# @class – только если класс уникален и не содержит динамических частей
# @role + другие атрибуты – хорош в комбинации (например, @role="button")
# data-tab, data-current – если значения статичны и уникальны

# Примеры:
# //button[@id="submit"]
# //div[@data-qa="user-profile"]

# ⚠️ Рискованные атрибуты (используйте осторожно)
# text() – может меняться (особенно в мультиязычных приложениях)
# @href – часто содержит динамические параметры
# Позиционные индексы ([1], [2]) – ненадёжны при изменениях DOM
# Динамические классы (например, button-123) – могут меняться после обновления страницы
# Примеры проблемных локаторов:
# //div[text()="Submit"] # ненадёжно, если текст изменится
# //a[contains(@href, "session")] # может сломаться при изменении URL
# //div[2]/span[1] # хрупкий при изменении структуры
