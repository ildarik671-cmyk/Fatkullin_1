# Ключевые символы в XPath
# // — поиск по всему документу (начиная от корня <html>)
# / — переход на следующий уровень вложенности (например, поиск элемента внутри другого элемента)

# По атрибуту:
# //элемент[@атрибут='значе
# ние']Пример: //input[@name='email']
# По тексту:
# //элемент[text()='текст'] (без @, так как текст — н
# е атрибут)Пример: //button[text()='Submit']

# Практические примеры
# //div[@class='table'] — находит <div> с классом table
# //employee[@id='2'] — находит <employee> с id="2"
# //name[text()='John'] — находит <name> с текстом John
# //employee[@id='2']/name — имя внутри <employee id="2">
# //designation[@discipline='web'] — находит элемент с атрибутом discipline="web"

# Комбинированные условия
# Пример://employee[@id='2']/designation[text()=';DBA Engineer']Находит designation с текстом DBA Engineer только у employee с id="2".

