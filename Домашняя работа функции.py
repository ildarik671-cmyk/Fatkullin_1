# **Задача 4:**
# Это будет самая простая демонстрация работы декоратора.
# 1.  Напишите простую функцию `say_hello()`, которая просто печатает: `"Привет!"`.
# 2.  Напишите декоратор `simple_decorator`, который перед вызовом оригинальной функции печатает строку
# `"Что-то делаю до..."`, а после вызова печатает `"Что-то делаю после..."`.
# 3.  Примените декоратор к функции `say_hello` (используй синтаксис `@simple_decorator`).
# 4.  Вызовите функцию `say_hello()`.
# Подсказка: тебе понадобится внутренняя функция-обёртка (`wrapper`).

def simple_decorator(func):
    def wrapper():
        print("Что-то делаю до...")
        func()
        print("Что-то делаю после...")
    return wrapper

@simple_decorator
def say_hello():
    print("Привет")
say_hello()


# **Задача 5:**
# Напишите декоратор `repeat_twice`, который вызывает декорируемую функцию два раза подряд.
# 1.  Создайте декоратор `repeat_twice`.
# 2.  Создайте функцию `greet`, которая принимает один аргумент `name` и печатает `"Привет, {name}!"`.
# 3.  Примените декоратор к функции `greet`.
# 4.  Вызовите `greet("Максим")`.
# **Важное отличие от предыдущей задачи:** твоя функция `greet` принимает аргумент. Это значит,
# что внутренняя функция `wrapper` в декораторе тоже должна принимать этот аргумент (или аргументы), чтобы передать их дальше в `func()`.
# Подсказка: для этого `wrapper` нужно определить как `def wrapper(name):`, а внутри вызывать `func(name)`.
# В более продвинутом варианте используют `*args` и `**kwargs`, но для этой задачи можно сделать и просто с одним параметром.

def repeat_twice(func):
    def wrapper(name):
        func(name)
        func(name)
    return wrapper
@repeat_twice
def greet(name):
    print(f"Привет, {name}!")
greet("Максим")





