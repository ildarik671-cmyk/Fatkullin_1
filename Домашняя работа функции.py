# **Задача 5.**
# Напиши простой декоратор `my_decorator`, который будет делать следующее:
# *   Перед вызовом декорируемой функции выводит: `"Начало работы функции."`
# *   После вызова декорируемой функции выводит: `"Функция завершила работу."`
# Примени этот декоратор к функции `my_function`, которая просто выводит `"Выполняется основная логика."`.

def my_decorator(fun):
    def wrapper():
        print("Начало работы функции.")
        fun()
        print("Функция завершила работу.")
    return wrapper
@my_decorator
def my_function():
    print("Выполняется основная логика.")


# **Задача 5 (вариант 2).**
# Напиши простой декоратор `show_time`, который будет делать следующее:
# *   Перед вызовом декорируемой функции выводит: `"Функция запущена."`
# *   После вызова декорируемой функции выводит: `"Функция выполнена."`
# Примени этот декоратор к функции `calculate()`, которая выводит `"Идут вычисления..."`.
# Убедись, что декоратор возвращает функцию-обёртку, а не вызывает её сразу.

def show_time(func):
    def wrapper():
        print("Функция запущена.")
        func()
        print("Функция выполнена.")
    return wrapper
@show_time
def calculate():
    print("Идут вычисления...")


# **Задача 5 (вариант 3).**
# Напиши простой декоратор `info_decorator`, который будет делать следующее:
# *   Перед вызовом декорируемой функции выводит: `"Выполняю функцию"`.
# *   После вызова декорируемой функции выводит: `"Функция выполнена"`.
# Примени этот декоратор к функции `say_hello()`, которая выводит `"Привет!"`.

def info_decorator(func):
    def wrapper():
        print("Выполняю функцию")
        func()
        print("Функция выполнена")
    return wrapper
@info_decorator
def say_hello():
    print("Привет!")


