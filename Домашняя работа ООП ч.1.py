# 4.  Метод `info`, который печатает строку в формате:
#     `"Прямоугольник {width}x{height}, площадь: {area}, периметр: {perimeter}"`
#     (значения `area` и `perimeter` нужно получить, вызвав соответствующие методы внутри `info`).
#
# После создания класса:
# 1.  Создай один прямоугольник с размерами 5 и 8.
# 2.  Вызови для него метод `info`.

class Rectangle:
    def __init__(self, width, height):
        self.width = width
        self.height = height
    def area(self):
        return self.width * self.height
    def perimeter(self):
        return  (self.width + self.height) * 2
    def info(self):
        print(f"Прямоугольник {self.width}x{self.height}, площадь: {self.area()}, периметр: {self.perimeter()}")
