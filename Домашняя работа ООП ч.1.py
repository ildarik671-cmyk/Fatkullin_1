# Создай класс `BankAccount`. У него должны быть:
# 1.  Конструктор (`__init__`), который принимает параметр `owner` (владелец) и сохраняет его как атрибут экземпляра.
# 2.  Атрибут экземпляра `balance` (баланс), который изначально равен 0 (установи в конструкторе).
# 3.  Метод `deposit`, который принимает сумму `amount` и добавляет её к `balance`. Метод также должен печатать сообщение: `"Внесено {amount} руб. Баланс: {balance} руб."`
# 4.  Метод `withdraw`, который принимает сумму `amount`. Если на счёте достаточно средств (`balance >= amount`),
# то он вычитает сумму и печатает: `"Снято {amount} руб. Баланс: {balance} руб."`. Если средств недостаточно, печатает: `"Недостаточно средств на счёте {owner}"`
# После создания класса:
# 1.  Создай один экземпляр счёта для любого владельца.
# 2.  Положи на счёт 500 рублей.
# 3.  Попробуй снять 200 рублей.
# 4.  Попробуй снять 400 рублей (должна появиться ошибка о недостатке средств).
# 5.  В конце просто вызови метод `deposit` с любой суммой, чтобы увидеть итоговый баланс.

class BankAccount:
    def __init__(self, owner, balance = 0):
        self.owner = owner
        self.balance = balance
    def deposit(self, amount):
        self.amount = amount
        self.balance += self.amount
        print(f"Внесено {self.amount} руб. Баланс: {self.balance} руб.")
    def withdraw(self, amount):
        self.amount = amount
        if self.balance >= self.amount:
            self.balance -= self.amount
            print(f"Снято {self.amount} руб. Баланс: {self.balance} руб.")
        else:
            print(f"Недостаточно средств на счёте {self.owner}")
ildar = BankAccount("Ildar")
ildar.deposit(500)
ildar.withdraw(200)
ildar.withdraw(400)


