class ToDoItem:
    def __init__(self, task, deadline):
        self.completed = False
        self.task = task
        self.deadline = deadline
    def mark_done(self):
        self.completed = True
    def show_info(self):
        if self.completed == False:
            print(f"[ ] {self.task} - сделать до {self.deadline}")
        else:
            print(f"[X] {self.task} - сделано")

