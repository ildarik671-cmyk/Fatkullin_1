# Задача 19 (while с пропуском)
# Напиши программу, которая с помощью цикла while выводит числа от 1 до 10,
# пропуская число 5 (то есть вывод: 1,2,3,4,6,7,8,9,10). Используй continue для пропуска.
# Подсказка: Внутри цикла проверь, если текущее число равно 5 — используй continue, чтобы перейти к следующей итерации, не выполняя print.

num = 0
while num <=10:
    num += 1
    if num == 5:
        continue
    elif num == 11:
        break
    print(num)



# Задача 19 (while с двумя условиями)
# Напиши программу, которая с помощью цикла while выводит чётные числа от 2 до 10 включительно, каждое с новой строки.
# Подсказка: Используй переменную-счётчик. В цикле проверяй, является ли текущее число чётным (остаток от деления на 2 равен 0).
# Если да — выводи его, затем увеличивай счётчик. Если нет — просто увеличивай счётчик без вывода.


num = 2
while num <= 10:
    if num % 2 == 0:
        print(num)
    num += 1


# Задача 20 (while с if-else)
