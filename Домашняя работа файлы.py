# **Задача №7 (Работа с файлами)**
# Напишите программу, которая:
# 1. Открывает файл `students.txt` в режиме записи (`"w"`).
# 2. Запрашивает у пользователя **три имени студента** (по очереди, через `input()`).
# 3. Записывает каждое введенное имя в файл с новой строки.
# 4. Используйте цикл `for` для трёх повторений.
# 5. Закройте файл (предпочтительно через контекстный менеджер).
# Пришлите ваш код для проверки.


with open("students.txt" , "w" , encoding = "utf-8") as student:
    for _ in range(3):
        name = input()
        student.write(name + "\n")




# Задача №9 (Контекстный менеджер with)
# Напишите программу, которая:
# Создаёт файл data.txt.
# Записывает в него строку: "Python".
# Затем, используя контекстный менеджер with, открывает этот же файл для чтения.
# Считывает содержимое и проверяет, равно ли оно строке "Python".
# Выводит на экран "Совпадает", если содержимое равно "Python", или "Не совпадает", если нет.
# Учитывайте, что метод .read() может вернуть строку с символом переноса строки \n в конце.
# Пришлите ваш код для проверки.


with open("data.txt", "w" , encoding = "utf-8") as file:
    file.write("Python")
with open("data.txt", "r" , encoding = "utf-8") as file:
    if file.read() == "Python":
        print("Совпадает")
    else:
        print("Не совпадает")

