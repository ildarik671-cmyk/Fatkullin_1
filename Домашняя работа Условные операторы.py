# Задача №9 (Комбинированная: вложенные условия и логические выражения)
# Напишите программу для проверки доступа к онлайн-контенту. Программа запрашивает:
# Возраст пользователя (age - целое число)
# Имеется ли подписка (has_subscription - строка "да" или "нет")
# Введен ли промокод (promo_code - строка, может быть любой, включая пустую)
# Правила доступа:
# Контент доступен, если пользователю 18 лет или больше.
# НО если у пользователя есть подписка ("да"), контент доступен с 16 лет.
# А если введен промокод "FREE18", контент доступен совершеннолетним (18+) без подписки, но несовершеннолетним с подпиской он НЕ отменяет правило про 16 лет.
# Программа должна вывести "Доступ разрешен" или "Доступ запрещен".
# Цель: Аккуратно учесть все условия и их приоритеты, используя вложенные условия или логические операторы.

age = int(input("Введи свой возраст:"))
has_subscription = input("Есть ли у тебя подписка?:")
promo_code = input("Введи промокод:")
if has_subscription == "да" or has_subscription == "нет":
    if age >= 18  or (has_subscription =="да" and age >= 16):
        print("Доступ разрешен")
    elif promo_code == "FREE18" and age >= 18 and has_subscription == "нет":
        print("Доступ разрешен")
    else:
        print("Доступ запрещен")


